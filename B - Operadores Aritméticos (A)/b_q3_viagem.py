# Escreva um programa que calcule quanto tempo levará uma viagem, dado a distância e a velocidade média.

# Entrada

velocidade = float(input('Insira a velocidade em km: '))
distancia = float(input('Insira a distancia em km: '))

# Processamento

tempo = distancia / velocidade

# Saída

print(f'O tempo de duração da viagem é ==> {tempo} horas')
