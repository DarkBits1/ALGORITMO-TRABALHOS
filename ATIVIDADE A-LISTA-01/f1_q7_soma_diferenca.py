# Leia 3 números, calcule e escreva a soma dos 2 primeiros e a diferença entre os 2 últimos.

# Entrada

numero1 = float(input('Insira o número 1: '))
numero2 = float(input('Insira o número 2: '))
numero3 = float(input('Insira o número 3: '))

# Processamento

soma = numero1 + numero2

diferenca = numero2 - numero3

#Saída

print(f'O valor da soma dos dois primeiros é ==> {soma:.2f}\n O valor da diferenca entre os 2 ultimos é {diferenca:.2f}')


