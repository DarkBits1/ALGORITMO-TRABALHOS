# Leia o valor da base e altura de um triângulo, calcule e escreva sua área. (área=(base * altura)/2)

# Entrada 

base = float(input('Insira o valor da base: '))
altura = float(input('Insira o valor da altura: '))

# Processamento 

area = (base * altura) / 2

# Saída

print(f'O valor da área desse triângulo é ==> {area}')
