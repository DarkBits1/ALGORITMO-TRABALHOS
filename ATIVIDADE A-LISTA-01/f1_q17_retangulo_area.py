# Leia o valor da base e altura de um retângulo, calcule e escreva sua área. (área = base * altura)

# Entrada

base = float(input('Insira o valor da base: '))
altura = float(input('Insira o valor da altura: '))

# Processamento

area = base * altura

# Saída

print(f'O valor da área desse retângulo é ==> {area:.2f}')
