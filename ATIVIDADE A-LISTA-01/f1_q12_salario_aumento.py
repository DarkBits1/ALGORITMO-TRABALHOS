# Leia o salário de um trabalhador e escreva seu novo salário com um aumento de 25%.

# Entrada

salario_original = float(input('Insira seu salário: '))

# Processamento

salario_novo = salario_original + (salario_original * 0.25)

# Saída 

print(f'Seu salário apos o aumento é ${salario_novo:.2f}')

